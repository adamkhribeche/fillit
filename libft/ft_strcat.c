#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	size_t	len;
	int		j;

	len = ft_strlen(s1);
	j = 0;
	while (s2[j])
	{
		s1[len++] = s2[j];
		j++;
	}
	s1[len] = '\0';
	return (s1);
}
