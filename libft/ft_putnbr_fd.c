#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == MIN_INT)
	{
		ft_putnbr_fd(-214748364, fd);
		n = 8;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n < 10)
		ft_putchar_fd(('0' + n), fd);
	else if (n >= 10)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
}
